package entities;

public class Email {

	//Encapsulates variable
	private String email;	
	
	//general email constructor
	public Email(String email) {
		this.email = email;
	}
		
	//Getters and Setters
	public String getEmail() {
		return email;
	}

	public void setEmail(String emails) {
		this.email = emails;
	}
	
}
